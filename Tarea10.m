clc;
clear;

x=[0, 0.75, 0.001];
%x=[0.9, 0.9, 6];
k=0;
e=1e-10;
l=10;
b=0.999;
bar=(-1/(-1-x(1)) -1/(-1-x(2)) -1/(x(1)-1) -1/(x(2)-1) -1/(-5.5*x(1)-3*x(2)-x(3)) -1/(-x(1)-x(3)));
%bar=(-1/(-1-x(1)) -1/(-1-x(2)) -1/(x(1)-1) -1/(x(2)-1) -1/(-1.3784*x(1)-4.9432*x(2)-x(3)) -1/(x(1)+5*x(2)-x(3)));
options=optimoptions(@fminunc, 'OptimalityTolerance', 1e-200);
while l*bar>e
    f=@(x) x(3)+l*(-1/(-1-x(1)) -1/(-1-x(2)) -1/(x(1)-1) -1/(x(2)-1) -1/(-5.5*x(1)-3*x(2)-x(3)) -1/(-x(1)-x(3)));
    %f=@(x) x(3)+l*(-1/(-1-x(1)) -1/(-1-x(2)) -1/(x(1)-1) -1/(x(2)-1) -1/(-1.3784*x(1)-4.9432*x(2)-x(3)) -1/(x(1)+5*x(2)-x(3)));
    x=fminunc(f, x, options);
    l=b*l;
    %bar=-1/(3*x(1)+x(2)-6)-1/(-4*x(1)+2*x(2)-4);
    bar=(-1/(-1-x(1)) -1/(-1-x(2)) -1/(x(1)-1) -1/(x(2)-1) -1/(-5.5*x(1)-3*x(2)-x(3)) -1/(-x(1)-x(3)));
    disp(l*bar)
    k=k+1;
end